def gausslegendre(a, b, n, params):
    c1,c2,c3,c4 = params
    l = b - a
    sum = 0
    for par in generatePoints(n):
        xi = 0.5*(a+b+par[0]*l)
        f = c1*(math.e**(c2*xi)) + c3*(xi**(c4))
        sum += (f * par[1])

    resultado = (l/2) * sum
    return resultado
