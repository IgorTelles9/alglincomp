import math

def generatePoints(n):
    if n == 2:
        return [[-0.57735,1.0],[0.57735,1]]

    if n == 3:
        return [[-0.77459,0.55555],[0.77459,0.55555],[0.0,0.88888]]
    
    if n == 4:
        return [[-0.86113,0.34785],[0.86113,0.34785],[-0.33998,0.65214],[0.33998,0.65214]]

    if n == 5:
        return [[-0.90617,0.23692],[0.90617,0.23692],[-0.53846,0.47862],[0.53846,0.47862],[0.0,0.56888]]

    if n == 6:
        return [[-0.93247,0.17132],[0.93247,0.17132],[-0.66121,0.36076],[0.66121,0.36076],[-0.23862,0.46791],[0.23862,0.46791]]

    if n == 7:
        return [[-0.9491,0.12498],[0.9491,0.12498],[-0.74153, 0.2797],[0.74153, 0.2797],[-0.40584, 0.38183],[0.40584, 0.38183],[0.0,0.41796]]

    if n == 8:
        return [[-0.96029, 0.10122],[0.96029, 0.10122],[-0.79666, 0.22238],[0.79666, 0.22238],[-0.52553, 0.31370],[0.52553, 0.31370],[-0.18343, 0.36268],[0.18343, 0.36268]]



def gausslegendre(a, b, n, c1, c2, c3, c4):
    l = b - a
    sum = 0
    for par in generatePoints(n):
        xi = 0.5*(a+b+par[0]*l)
        f = c1*(math.e**(c2*xi)) + c3*(xi**(c4))
        sum += (f * par[1])

    resultado = (l/2) * sum
    return resultado


